function getColorFromPercent(percent) {
    // 例如，让颜色在蓝色和红色之间变化
    var startColor = [0, 0, 255];  // 蓝色
    var endColor = [255, 0, 0];    // 红色

    var color = [];
    for (var i = 0; i < 3; i++) {
        color[i] = Math.round(startColor[i] + percent * (endColor[i] - startColor[i]));
    }

    return 'rgb(' + color.join(',') + ')';
}
